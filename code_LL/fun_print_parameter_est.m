function fun_print_parameter_est(prms_est)
% PARAMETERS
% k_1on [01]; k_1off [02]; k_2 [03];   k_3on [04];  k_3off [05];
% k_4on [06]; k_4off [07]; k_5on [08]; k_5off [09];
% k_6on [10]; k_6off [11]; k_7on [12]; k_7off [13];
% k_8on [14]; k_8off [15]; sigma [16];
switch numel(prms_est)
    case 15
    KD_vec = [prms_est(2)/prms_est(1),...               % KD1
            (prms_est(2)+prms_est(3))/prms_est(1),...   % K_M
            prms_est(5)/prms_est(4),...                 % KD3
            prms_est(7)/prms_est(6),...                 % KD4
            prms_est(9)/prms_est(8),...                 % KD5
            prms_est(11)/prms_est(10),...               % KD6
            prms_est(13)/prms_est(12),...               % KD7
            prms_est(15)/prms_est(14)];...              % KD8
    
    fprintf(['KD1: %0.2E,    K_M: %0.2E\n',...  
                'KD3: %0.2E,    KD4: %0.2E \n',...  
                    'KD5: %0.2E,    KD6: %0.2E \n',...   
                        'KR7: %0.2E,    KD8: %0.2E \n'],KD_vec);
    case 16
    fprintf(['k_1on: %0.3E,    k_1off: %0.2E\n', ...
        'k_cat: %0.3E, \n',...  
        'k_3on: %0.3E,    k_3off: %0.2E \n',...  
        'k_4on: %0.3E,    k_4off: %0.2E \n',...   
        'k_5on: %0.3E,    k_5off: %0.2E \n',...  
        'k_6on: %0.3E,    k_6off: %0.2E \n',...  
        'k_7on: %0.3E,    k_7off: %0.2E \n',...  
        'k_8on: %0.3E,    k_8off: %0.2E \n',...
        'sigma: %0.2E \n'],prms_est);
    case 17
    fprintf(['k_1on: %0.3E,    k_1off: %0.2E,\n',...
        'k_cat: %0.2E, \n',...  
        'k_3on: %0.3E,    k_3off: %0.2E \n',...  
        'k_4on: %0.3E,    k_4off: %0.2E \n',...   
        'k_5on: %0.3E,    k_5off: %0.2E \n',...  
        'k_6on: %0.3E,    k_6off: %0.2E \n',...  
        'k_7on: %0.3E,    k_7off: %0.2E \n',...  
        'k_8on: %0.3E,    k_8off: %0.2E,\n',...
        'sigma: %0.2E LogL: %0.2E\n'],prms_est);
end